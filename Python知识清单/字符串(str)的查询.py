arr='hello,hello'
print(arr.index('lo'))        #3
print(arr.find('lo'))         #3
print(arr.rindex('lo'))       #9 
print(arr.rfind('lo'))        #9
#字符串的查询
#index()   查找子串第一次出现的位置，如果找不到就报错
#rindex()  查找子串最后一次出现的位置，如果找不到就报错
#find()    查找子串第一次出现的位置，如果找不到就返回-1
#rfind()   查找子串最后一次出现的位置，如果找不到就返回-1
