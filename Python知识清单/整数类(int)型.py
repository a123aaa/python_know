a=5        #十进制 <class 'int'>
b=0o11     #八进制 <class 'int'>
c=0x11     #十六进制 <class 'int'>
d=0b11     #二进制 <class 'int'>
print('a=',a,type(a))    #a= 5 <class 'int'>
print('b=',b,type(b))    #b= 9 <class 'int'>
print('c=',c,type(c))    #c= 17 <class 'int'>
print('d=',d,type(d))    #d= 3 <class 'int'>
