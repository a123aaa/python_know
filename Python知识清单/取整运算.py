print( 9//2 )                   #4
print( -9//-2 )                 #4
print( -9//2 )                  #-5            #取整运算:一负一正向下取，同下结果一样
print( 9//-2 )                  #-5            #取整运算:一负一正向下取，同上结果一样
print( -9%2 )                   #1             #取余运算公式:被除数-除数*取整商=>>(-9)- 2  *(-5)==1
print( 9%-2 )                   #-1            #取余运算公式:被除数-除数*取整商=>>   9-(-2)*(-5)==-1 
