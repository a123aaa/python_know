#可变序列：集合，列表，字典。对他们进行增删改后，储存地址不变
#不可变序列：字符串，元组。对他们进行增删改后，储存地址改变
#数据结构       是否可变       是否重复       是否有序       定义符号
#列表(list)       可变          可重复          有序           []
#元组(tuple)     不可变         可重复          有序           () 
#字典(dict)       可变   键不可重复，值可重复   无序       {key : value}
#集合(set)        可变         不可重复         无序           {}

