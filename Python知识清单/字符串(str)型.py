print('我爱你，zzy')              #我爱你，zzy               #用单引号' '只能是一行
print("我爱你，zzy")              #我爱你，zzy               #用双引号" "只能是一行
print('''我爱你，zzy''')          #我爱你，zzy               #用三单引号'''  '''只能是一行
print("""我爱你，zzy""")          #我爱你，zzy               #用三双引号"""  """只能是一行
print('''我爱你，                 #我爱你，                  #可以换行
         zzy''')                  #zzy
print("""我爱你，                 #我爱你，                  #可以换行
         zzy""")                  #zzy
print(type('我爱你'))             #<class 'str'>
