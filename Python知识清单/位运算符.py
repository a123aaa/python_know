#&；对二进制进行运算，都1才1，有0则0
#|：对二进制进行运算，有1则1，都0才0
a=4                          #00000100
b=8                          #00001000
print(a&b)          #0       #00000000
print(a|b)          #12      #00001100

#<<：对二进制进行运算，左移操作符---高位溢出，低位补0，相当于x2
#>>：对二进制进行运算，右移操作符---低位溢出，高位补0，相当于/2
a=4                         #00000100
print(a<<1)         #8     0#0000100 |
b=8                         #00001000 
print(b>>1)         #4      # 0000100|0
